    private void erzeugeEigenschaften() {
        
        for(String zeile : txaEigenschaften.getText().split("\n")){
                String[] split = zeile.split(",");
                Eigenschaft e = new Eigenschaft();
                e.setName(split[0]);
                e.setDatentyp(split[1]);
                e.setEditierbar(split[2]);
                _eigenschaften.add(e);
        }
        _tableModel.fireTableDataChanged();
    }
    
